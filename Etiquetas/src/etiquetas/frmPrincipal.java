    private void validarNumero(String texto, java.awt.event.KeyEvent evt) {
        if (texto.length() == 12) {
            evt.consume();
        }
        int key = (int) evt.getKeyChar();
        if (key < 48 || key > 57) {
            evt.consume();
        }
    }
    private void reporte() {
        try {

            URL url = this.getClass().getResource("rptEtiquetas.jasper");
            String dir = ClassLoader.getSystemResource("etiquetas/logo.jpg").toString();

            JasperReport masterReport = (JasperReport) JRLoader.loadObject(url);
            Map parametro = new HashMap();
            parametro.clear();
            parametro.put("SUBREPORT_DIR", "/");
            parametro.put("logo_p", dir);

            JasperPrint jasperPrint = JasperFillManager.fillReport(masterReport, parametro, new JRBeanCollectionDataSource(datos));

            JRViewer jrv = new JRViewer(jasperPrint);
            jrv.setBounds(6, 6, 720, 470);
            jrv.setVisible(true);

            JFrame jFrame = new JFrame();
            jFrame.setBounds(6, 6, 720, 470);
            jFrame.setLocationRelativeTo(null);
            jFrame.setResizable(false);
            jFrame.add(getContentPane().add(jrv));
            jFrame.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        clsEtiquetas etiquetas = new clsEtiquetas();
        ResultSet rs = null;

        if (rbtCodigo.isSelected()) {

            if (txtDesde.getText().equals("") || txtHasta.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe ingresar ambos codigos de activo");
                return;
            }
           
            Long desde = Long.parseLong(txtDesde.getText());
            Long hasta = Long.parseLong(txtHasta.getText());

            if (desde > hasta) {
                JOptionPane.showMessageDialog(null, "El codigo desde debe ser menor o igual al codigo hasta");
                return;
            }

            if (txtDesde.getText().length() < 12) {
                JOptionPane.showMessageDialog(null, "El codigo desde debe ser de 12 digitos");
                return;
            }

            if (txtHasta.getText().length() < 12) {
                JOptionPane.showMessageDialog(null, "El codigo hasta debe ser de 12 digitos");
                return;
            }

            /*..................................................................*/
            rs = etiquetas.mostrarActivos(desde.toString(), hasta.toString());

        } 
        
        if (rbtFecha.isSelected()) {       
            utilidades convertir = new utilidades();
            String fecha1 = convertir.convertDateToString(jDateChooser1.getDate());
            String fecha2 = convertir.convertDateToString(jDateChooser2.getDate());

            if (fecha1 == null || fecha2 == null) {
                JOptionPane.showMessageDialog(null, "Debe ingresar ambas fechas");
                jButton1.setIcon(null);
                return;
            }

            Date inicio = Date.valueOf(fecha1);
            Date fin = Date.valueOf(fecha2);

            if (fin.before(inicio)) {
                JOptionPane.showMessageDialog(null, "La fecha desde debe ser menor o igual a la fecha hasta");
                return;
            }
            rs = etiquetas.mostrarActivos2(inicio, fin);
        }
        if (rbtCentroCosto.isSelected()) {  
            if(txtCentroCosto.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Debe buscar una dependencia");
                return;
            }
            
            rs = etiquetas.mostrarActivosDependencia(TIPO_UBICAC , COD_UBICAC);
        }
        num_total_registros = 0;
        informe.clear();
        limpiarTabla();
        try {
            while (rs.next()) {
                informe.add(new bnEtiquetas(rs.getString("CODIGO_ACTIVO"), rs.getString("CODIGO_ACTIVO").substring(0, 8) + "-" + rs.getString("CODIGO_ACTIVO").substring(8, 12),
                        rs.getString("DESCRIPCION"), rs.getString("UBICAC_FISICA")));
                num_total_registros++;

            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "mal");
            // Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (num_total_registros == 0) {
            JOptionPane.showMessageDialog(null, "No se encontraron registros");
            btnImprimir.setEnabled(false);
            limpiarTabla();
            return;
        }
        btnImprimir.setEnabled(true);
        agreagarRegistros(num_total_registros);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void agreagarRegistros(int num_total_registros) {
        int count = 1;
        for (int i = 0; i < num_total_registros; i++) {

            Object[] fila = new Object[4];
            fila[0] = count;
            fila[1] = informe.get(i).getCODIGO_ACTIVO();
            fila[2] = informe.get(i).getDESCRIPCION();
            fila[3] = informe.get(i).getUBICACION();

            miTabla.addRow(fila);
            count++;
        }
    }

    private void limpiarTabla() {
        try {
            int filas = miTabla.getRowCount() - 1;

            if (filas != -1) {
                for (int i = filas; 0 <= i; i--) {
                    miTabla.removeRow(i);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }

